var distinctNames = function (ideas) {
  const aCode = 'a'.codePointAt(0);

  const groups = ideas.reduce((groups, idea) => {
    const key = idea.slice(1);
    groups[key] ||= 0;
    groups[key] |= 1 << (idea.codePointAt(0) - aCode);
    return groups;
  }, {})

  const providesAccepts = new Array(26).fill(0).map(() => new Array(26).fill(0));

  return ideas.reduce((total, idea) => {
    const firstLetter = idea.codePointAt(0) - aCode;
    const taken = groups[idea.slice(1)];

    let nextTotal = total;
    for (let letter = 0; letter < 26; letter += 1) {
      if (taken & 1 << letter) continue;

      // 2 * because if we can match two ideas, then we can order them `a b` or `b a`
      nextTotal += 2 * providesAccepts[letter][firstLetter];
      providesAccepts[firstLetter][letter] += 1;
    }
    return nextTotal;
  }, 0)
};

